/// Handler for POST /v1/completions
///
/// Validates the request against the legacy Completions schema, then forwards
/// to the upstream provider's `/v1/completions` endpoint.
pub async fn completions_handler<T: HttpClient + Clone + Send + Sync + 'static>(
    State(state): State<AppState<T>>,
    headers: HeaderMap,
    body: Bytes,
) -> Response {
    // Validate a throwaway copy; forward the original bytes verbatim (COR-522).
    let request: CompletionRequest = match serde_json::from_slice(&body) {
        Ok(r) => r,
        Err(e) => return invalid_request_response(&e),
    };
    let unsupported_reasoning_param = [
        (request.reasoning_effort.is_some(), "reasoning_effort"),
        (request.reasoning.is_some(), "reasoning"),
        (request.thinking.is_some(), "thinking"),
        (
            request.thinking_token_budget.is_some(),
            "thinking_token_budget",
        ),
        (
            request.chat_template_kwargs.is_some(),
            "chat_template_kwargs",
        ),
    ]
    .into_iter()
    .find_map(|(present, param)| present.then_some(param));
    if let Some(param) = unsupported_reasoning_param {
        let message = format!(
            "Parameter '{param}' is not supported by /v1/completions. Use /v1/chat/completions or /v1/responses."
        );
        return OnwardsErrorResponse::invalid_request(
            &message,
            Some(param),
            "unsupported_parameter",
        )
        .into_response();
    }

    let original_model = request.model.clone();
    let is_streaming = request.stream.unwrap_or(false);

    debug!(
        model = %original_model,
        stream = is_streaming,
        "Completions request validated"
    );

    let resolved_model = extract_model_from_request(&headers, &body).unwrap_or(original_model);
    let ForwardResult {
        response,
        trusted,
        internal_error,
    } = forward_request(state, headers, "/completions", body.to_vec()).await;

    if response.status().is_success() {
        let response_is_sse = response_is_sse(&response);

        if is_streaming || response_is_sse {
            sanitize_streaming_completions_response(response, resolved_model, trusted).await
        } else {
            sanitize_completions_response(response, resolved_model).await
        }
    } else if trusted || internal_error {
        debug!(model = %resolved_model, "Bypassing error sanitization for trusted provider");
        response
    } else {
        sanitize_error_response(response).await
    }
}

/// Forward a validated request to the upstream provider
async fn forward_request<T: HttpClient + Clone + Send + Sync + 'static>(
    state: AppState<T>,
    mut headers: HeaderMap,
    path: &str,
    body_bytes: Vec<u8>,
) -> ForwardResult {
    // Ensure content-type is set
    headers.insert(
        axum::http::header::CONTENT_TYPE,
        "application/json".parse().unwrap(),
    );

    // Disable compression - hyper doesn't auto-decompress, causing parse failures
    headers.remove(axum::http::header::ACCEPT_ENCODING);

    // Build the request to forward
    let mut request_builder = Request::builder().method("POST").uri(path);

    // Copy headers to the request
    for (name, value) in headers.iter() {
        request_builder = request_builder.header(name, value);
    }

    let request = match request_builder.body(Body::from(body_bytes)) {
        Ok(req) => req,
        Err(e) => {
            error!(error = %e, "Failed to build request");
            let response = error_response(
                StatusCode::INTERNAL_SERVER_ERROR,
                "server_error",
                "Failed to build request",
            );
            return ForwardResult {
                response,
                trusted: false,
                internal_error: true,
            };
        }
    };

    // Use the existing target message handler
    let (response, internal_error) = match target_message_handler(State(state), request).await {
        Ok(response) => (response, false),
        Err(err) => (err.into_response(), true),
    };

    let trusted = response
        .extensions()
        .get::<ResolvedTrust>()
        .map(|t| t.0)
        .unwrap_or(false);

    ForwardResult {
        response,
        trusted,
        internal_error,
    }
}

/// Create an OpenAI-compatible error response
///
/// Format matches OpenAI's error structure:
/// https://platform.openai.com/docs/guides/error-codes
fn error_response(status: StatusCode, error_type: &str, message: &str) -> Response {
    let body = json!({
        "error": {
            "message": message,
            "type": error_type,
            "param": null,
            "code": null
        }
    });

    (status, Json(body)).into_response()
}

/// Strict-mode request validation error, matching what the `Json` extractor
/// used to return: `422 Unprocessable Entity` for a valid-JSON body that fails
/// the schema (missing/typed field), `400 Bad Request` for malformed JSON.
fn invalid_request_response(e: &serde_json::Error) -> Response {
    let status = if e.is_data() {
        StatusCode::UNPROCESSABLE_ENTITY
    } else {
        StatusCode::BAD_REQUEST
    };
    error_response(
        status,
        "invalid_request_error",
        &format!("Invalid request: {e}"),
    )
}

/// Sanitize non-streaming chat completion response
///
/// Deserializes the response through our strict schema (drops extra fields),
/// rewrites the model field, and re-serializes.
async fn sanitize_chat_response(mut response: Response, original_model: String) -> Response {
    // Read the response body
    let body_bytes =
        match axum::body::to_bytes(std::mem::take(response.body_mut()), usize::MAX).await {
            Ok(bytes) => {
                // ZDR: log only the length, never the response body content.
                debug!(
                    bytes_read = bytes.len(),
                    "Read upstream response body for sanitization"
                );
                bytes
            }
            Err(e) => {
                error!(error = %e, "Failed to read response body for sanitization");
                return error_response(
                    StatusCode::BAD_GATEWAY,
                    "api_error",
                    "Failed to read upstream response",
                );
            }
        };

    let mut raw_response: serde_json::Value = match serde_json::from_slice(&body_bytes) {
        Ok(resp) => resp,
        Err(e) => {
            // Failed to deserialize - provider returned malformed response
            // Log the error but return standard error instead of passing through
            // ZDR: never log the body — only its length and the parse error.
            error!(
                error = %e,
                response_len = body_bytes.len(),
                "Failed to deserialize chat response from provider, returning standard error"
            );
            return error_response(StatusCode::BAD_GATEWAY, "api_error", "Bad gateway");
        }
    };

    normalize_chat_completion_response_value(&mut raw_response, &original_model);

    // Deserialize through our strict schema (automatically drops extra fields)
    let mut chat_response: ChatCompletionResponse = match serde_json::from_value(raw_response) {
        Ok(resp) => resp,
        Err(e) => {
            error!(
                error = %e,
                "Failed to coerce chat response into strict schema"
            );
            return error_response(StatusCode::BAD_GATEWAY, "api_error", "Bad gateway");
        }
    };

    // Rewrite model field to match original request
    chat_response.model = original_model;

    // Re-serialize with only our defined fields
    match serde_json::to_vec(&chat_response) {
        Ok(sanitized_bytes) => {
            // Set Content-Length to match the new sanitized body size
            let content_length = sanitized_bytes.len();
            // ZDR: log only the length, never the sanitized body content.
            debug!(
                content_length = content_length,
                "Setting sanitized response body"
            );
            *response.body_mut() = Body::from(sanitized_bytes);

            // Remove Transfer-Encoding since we're setting Content-Length
            // HTTP doesn't allow both headers - having both causes undefined behavior
            response
                .headers_mut()
                .remove(axum::http::header::TRANSFER_ENCODING);
            response.headers_mut().insert(
                header::CONTENT_LENGTH,
                header::HeaderValue::from(content_length),
            );
            debug!("Sanitized non-streaming chat completion response");
            response
        }
        Err(e) => {
            error!(
                error = %e,
                "Failed to serialize sanitized chat response, returning standard error"
            );
            error_response(
                StatusCode::INTERNAL_SERVER_ERROR,
                "api_error",
                "Internal server error",
            )
        }
    }
}

/// Sanitize streaming chat completion response
///
/// Processes each SSE chunk line-by-line, deserializes through our strict schema,
/// rewrites the model field, and re-serializes. Ignores comment lines and other
/// non-data SSE fields to prevent metadata leakage.
///
/// Also detects provider error objects embedded in the SSE stream (e.g. when a
/// provider returns HTTP 200 but sends `{"error": ...}` instead of a valid
/// chunk). These are forwarded as clean SSE events — sanitized for untrusted
/// providers — so that downstream consumers like fusillade can detect them in the
/// reassembled body rather than seeing a broken stream.
async fn sanitize_streaming_chat_response(
    mut response: Response,
    original_model: String,
    trusted: bool,
) -> Response {
    // Wrap with SseBufferedStream to ensure we receive complete SSE events (delimited by \n\n).
    // Providers may send partial chunks that split JSON across network packets.
    // This buffering ensures we can successfully parse JSON in each event.
    let body_stream =
        http_body_util::BodyExt::into_data_stream(std::mem::take(response.body_mut()));
    let buffered_stream = crate::sse::SseBufferedStream::new(body_stream);
    let stream_fallback_id = generated_chat_completion_id();

    let sanitized_stream = buffered_stream.map(move |chunk_result| {
        match chunk_result {
            Ok(chunk) => {
                let chunk_str = String::from_utf8_lossy(&chunk);
                let mut sanitized_lines = Vec::new();

                // Process SSE line-by-line for streaming chunks,
                // preserving empty lines and ignoring comment/event-type lines
                for line in chunk_str.lines() {
                    if let Some(data_part) = line.strip_prefix("data: ") {
                        // This is a data line

                        // Handle [DONE] marker
                        if data_part.trim() == "[DONE]" {
                            sanitized_lines.push(line.to_string());
                            continue;
                        }

                        // Deserialize the chunk through our strict schema.
                        // Parse once here: an embedded error envelope and a
                        // normal chunk are both valid JSON, so this same parse
                        // feeds the error check below and the typed coercion.
                        let mut raw_chunk: serde_json::Value = match serde_json::from_str(data_part)
                        {
                            Ok(chunk) => chunk,
                            Err(e) => {
                                error!(
                                    error = %e,
                                    data_len = data_part.len(), // ZDR: length only, never SSE chunk content
                                    "Failed to parse SSE data line from provider, terminating stream"
                                );
                                return Err(std::io::Error::other(
                                    "Malformed SSE data from provider",
                                ));
                            }
                        };

                        // Provider error envelopes may appear either as the
                        // entire chunk (`{"error":{...}}`) or alongside
                        // otherwise valid chunk fields (OpenRouter shape:
                        // `{"id":...,"choices":[],"error":{...}}`). Detect
                        // them before strict coercion — the strict
                        // `ChatCompletionChunk` schema has no `error` field
                        // and would silently drop it, leaving the
                        // downstream reassembler with no signal.
                        if let Some(error_event) = try_format_sse_error(&raw_chunk, trusted) {
                            error!(
                                data_len = data_part.len(), // ZDR: length only, never SSE chunk content
                                "Provider returned error envelope in SSE chunk, forwarding as error event"
                            );
                            sanitized_lines.push(error_event);
                            continue;
                        }

                        normalize_chat_completion_chunk_value(
                            &mut raw_chunk,
                            &original_model,
                            &stream_fallback_id,
                        );

                        match serde_json::from_value::<ChatCompletionChunk>(raw_chunk) {
                            Ok(mut chunk_data) => {
                                // Rewrite model field
                                chunk_data.model = original_model.clone();

                                // Re-serialize
                                match serde_json::to_string(&chunk_data) {
                                    Ok(sanitized_json) => {
                                        sanitized_lines.push(format!("data: {}", sanitized_json));
                                    }
                                    Err(e) => {
                                        error!(error = %e, "Failed to serialize chunk, terminating stream");
                                        return Err(std::io::Error::other(
                                            "Failed to serialize chunk",
                                        ));
                                    }
                                }
                            }
                            Err(e) => {
                                error!(
                                    error = %e,
                                    data_len = data_part.len(), // ZDR: length only, never SSE chunk content
                                    "Failed to parse SSE data line from provider, terminating stream"
                                );
                                return Err(std::io::Error::other(
                                    "Malformed SSE data from provider",
                                ));
                            }
                        }
                    } else if line.is_empty() {
                        // Preserve empty lines (SSE event delimiters)
                        sanitized_lines.push(String::new());
                    }
                    // Ignore all other lines (comments, event types, etc.) - don't leak metadata
                }

                // Reconstruct the chunk with sanitized content
                // Preserve trailing newlines from original chunk
                let mut sanitized_chunk = sanitized_lines.join("\n");

                // Count trailing newlines in input and restore them
                let input_trailing = chunk_str.chars().rev().take_while(|&c| c == '\n').count();
                let output_trailing = sanitized_chunk
                    .chars()
                    .rev()
                    .take_while(|&c| c == '\n')
                    .count();

                for _ in output_trailing..input_trailing {
                    sanitized_chunk.push('\n');
                }

                Ok::<_, std::io::Error>(axum::body::Bytes::from(sanitized_chunk))
            }
            Err(e) => {
                error!(error = %e, "Stream error");
                Err(std::io::Error::other(e))
            }
        }
    });

    *response.body_mut() = Body::from_stream(sanitized_stream);
    // Remove Content-Length header - streaming responses should use chunked encoding
    response.headers_mut().remove(header::CONTENT_LENGTH);
    debug!("Set up streaming chat completion response sanitization");
    response
}

/// Sanitize non-streaming completions response
///
/// Deserializes through `CompletionResponse` (drops extra fields), rewrites the
/// model field, and re-serializes.
async fn sanitize_completions_response(mut response: Response, original_model: String) -> Response {
    let body_bytes =
        match axum::body::to_bytes(std::mem::take(response.body_mut()), usize::MAX).await {
            Ok(bytes) => bytes,
            Err(e) => {
                error!(error = %e, "Failed to read completions response body");
                return error_response(
                    StatusCode::BAD_GATEWAY,
                    "api_error",
                    "Failed to read upstream response",
                );
            }
        };

    let mut raw_response: serde_json::Value = match serde_json::from_slice(&body_bytes) {
        Ok(resp) => resp,
        Err(e) => {
            error!(
                error = %e,
                response_len = body_bytes.len(), // ZDR: length only, never response body content
                "Failed to deserialize completions response from provider, returning standard error"
            );
            return error_response(StatusCode::BAD_GATEWAY, "api_error", "Bad gateway");
        }
    };

    normalize_completion_response_value(&mut raw_response, &original_model);

    let mut completion_response: CompletionResponse = match serde_json::from_value(raw_response) {
        Ok(resp) => resp,
        Err(e) => {
            error!(
                error = %e,
                "Failed to coerce completions response into strict schema"
            );
            return error_response(StatusCode::BAD_GATEWAY, "api_error", "Bad gateway");
        }
    };

    completion_response.model = original_model;

    match serde_json::to_vec(&completion_response) {
        Ok(sanitized_bytes) => {
            let content_length = sanitized_bytes.len();
            *response.body_mut() = Body::from(sanitized_bytes);
            response
                .headers_mut()
                .remove(axum::http::header::TRANSFER_ENCODING);
            response.headers_mut().insert(
                header::CONTENT_LENGTH,
                header::HeaderValue::from(content_length),
            );
            debug!("Sanitized non-streaming completions response");
            response
        }
        Err(e) => {
            error!(error = %e, "Failed to serialize sanitized completions response");
            error_response(
                StatusCode::INTERNAL_SERVER_ERROR,
                "api_error",
                "Internal server error",
            )
        }
    }
}

/// Sanitize streaming completions response
///
/// Processes each SSE chunk line-by-line, deserializes through `CompletionChunk`,
/// rewrites the model field, and re-serializes.
///
/// Also forwards provider error objects embedded in the SSE stream as clean
/// events (see [`sanitize_streaming_chat_response`] for details).
async fn sanitize_streaming_completions_response(
    mut response: Response,
    original_model: String,
    trusted: bool,
) -> Response {
    let body_stream =
        http_body_util::BodyExt::into_data_stream(std::mem::take(response.body_mut()));
    let buffered_stream = crate::sse::SseBufferedStream::new(body_stream);
    let stream_fallback_id = generated_completion_id();

    let sanitized_stream = buffered_stream.map(move |chunk_result| {
        match chunk_result {
            Ok(chunk) => {
                let chunk_str = String::from_utf8_lossy(&chunk);
                let mut sanitized_lines = Vec::new();

                for line in chunk_str.lines() {
                    if let Some(data_part) = line.strip_prefix("data: ") {
                        if data_part.trim() == "[DONE]" {
                            sanitized_lines.push(line.to_string());
                            continue;
                        }

                        let mut raw_chunk: serde_json::Value = match serde_json::from_str(data_part)
                        {
                            Ok(chunk) => chunk,
                            Err(e) => {
                                error!(
                                    error = %e,
                                    data_len = data_part.len(), // ZDR: length only, never SSE chunk content
                                    "Failed to parse completions chunk, terminating stream"
                                );
                                return Err(std::io::Error::other(
                                    "Malformed SSE data from provider",
                                ));
                            }
                        };

                        // See `sanitize_streaming_chat_response` for the full
                        // rationale — detecting embedded provider errors
                        // before strict coercion keeps OpenRouter-shape chunks
                        // (normal fields + `error`) from getting their error
                        // silently stripped. Reuses the parse above.
                        if let Some(error_event) = try_format_sse_error(&raw_chunk, trusted) {
                            error!(
                                data_len = data_part.len(), // ZDR: length only, never SSE chunk content
                                "Provider returned error envelope in SSE chunk, forwarding as error event"
                            );
                            sanitized_lines.push(error_event);
                            continue;
                        }

                        normalize_completion_chunk_value(
                            &mut raw_chunk,
                            &original_model,
                            &stream_fallback_id,
                        );

                        match serde_json::from_value::<CompletionChunk>(raw_chunk) {
                            Ok(mut chunk_data) => {
                                chunk_data.model = original_model.clone();
                                match serde_json::to_string(&chunk_data) {
                                    Ok(json) => sanitized_lines.push(format!("data: {json}")),
                                    Err(e) => {
                                        error!(error = %e, "Failed to serialize completions chunk, terminating stream");
                                        return Err(std::io::Error::other(
                                            "Failed to serialize completions chunk",
                                        ));
                                    }
                                }
                            }
                            Err(e) => {
                                error!(
                                    error = %e,
                                    data_len = data_part.len(), // ZDR: length only, never SSE chunk content
                                    "Failed to parse completions chunk, terminating stream"
                                );
                                return Err(std::io::Error::other(
                                    "Malformed SSE data from provider",
                                ));
                            }
                        }
                    } else if line.is_empty() {
                        // Preserve empty lines (SSE event delimiters)
                        sanitized_lines.push(String::new());
                    }
                    // Ignore comment lines and other non-data SSE fields
                }

                // Restore trailing newlines from the original chunk
                let mut sanitized_chunk = sanitized_lines.join("\n");
                let input_trailing =
                    chunk_str.chars().rev().take_while(|&c| c == '\n').count();
                let output_trailing = sanitized_chunk
                    .chars()
                    .rev()
                    .take_while(|&c| c == '\n')
                    .count();
                for _ in output_trailing..input_trailing {
                    sanitized_chunk.push('\n');
                }

                Ok::<_, std::io::Error>(axum::body::Bytes::from(sanitized_chunk))
            }
            Err(e) => {
                error!(error = %e, "Stream error");
                Err(std::io::Error::other(e))
            }
        }
    });

    *response.body_mut() = Body::from_stream(sanitized_stream);
    response.headers_mut().remove(header::CONTENT_LENGTH);
    debug!("Set up streaming completions response sanitization");
    response
}

/// Sanitize embeddings response
///
/// Deserializes the response through our strict schema (drops extra fields),
/// rewrites the model field, and re-serializes.
async fn sanitize_embeddings_response(mut response: Response, original_model: String) -> Response {
    let body_bytes =
        match axum::body::to_bytes(std::mem::take(response.body_mut()), usize::MAX).await {
            Ok(bytes) => bytes,
            Err(e) => {
                error!(error = %e, "Failed to read embeddings response body");
                return error_response(
                    StatusCode::BAD_GATEWAY,
                    "api_error",
                    "Failed to read upstream response",
                );
            }
        };

    let mut embeddings_response: EmbeddingsResponse = match serde_json::from_slice(&body_bytes) {
        Ok(resp) => resp,
        Err(e) => {
            error!(
                error = %e,
                response_len = body_bytes.len(), // ZDR: length only, never response body content
                "Failed to deserialize embeddings response from provider, returning standard error"
            );
            return error_response(StatusCode::BAD_GATEWAY, "api_error", "Bad gateway");
        }
    };

    // Rewrite model field
    embeddings_response.model = original_model;

    match serde_json::to_vec(&embeddings_response) {
        Ok(sanitized_bytes) => {
            // Set Content-Length to match the new sanitized body size
            let content_length = sanitized_bytes.len();
            *response.body_mut() = Body::from(sanitized_bytes);

            // Remove Transfer-Encoding since we're setting Content-Length
            // HTTP doesn't allow both headers - having both causes undefined behavior
            response
                .headers_mut()
                .remove(axum::http::header::TRANSFER_ENCODING);
            response.headers_mut().insert(
                header::CONTENT_LENGTH,
                header::HeaderValue::from(content_length),
            );
            debug!("Sanitized embeddings response");
            response
        }
        Err(e) => {
            error!(
                error = %e,
                "Failed to serialize sanitized embeddings response, returning standard error"
            );
            error_response(
                StatusCode::INTERNAL_SERVER_ERROR,
                "api_error",
                "Internal server error",
            )
        }
    }
}

/// Detect a provider error envelope embedded in an SSE data payload and
/// return it formatted as a stand-alone SSE data line.
///
/// Two distinct provider shapes get caught here:
///
/// 1. **Error-only chunks** — e.g. our own inference endpoints emit
///    `data: {"error":{"message":"Engine was shut down...","code":500}}`
///    when a worker dies mid-generation.
/// 2. **Chunks that carry both completion fields *and* an `error` object** —
///    OpenRouter (and similar third-party gateways) returns HTTP 200 with
///    chunks like `{"id":"...","choices":[],"error":{"code":429,...}}` when
///    a downstream provider returns 429. Without this detector the strict
///    `ChatCompletionChunk` deserializer would silently drop the `error`
///    field (it's a flatten-style unknown) and the downstream reassembler
///    would see a content-less stream — surfacing as a control-layer 5xx.
///
/// The emitted line is always a bare `{"error": {...}}` envelope (no
/// surrounding chunk wrapper) so that downstream consumers like
/// `fusillade::http`'s `event.data.starts_with("{\"error\"")` reclassifier
/// match it and rewrite the HTTP status to the embedded `code`.
///
/// `value` is the already-parsed chunk JSON (the caller parses once and
/// reuses the value for typed coercion, avoiding a second parse on the
/// SSE hot path).
///
/// Trust gating:
/// - `trusted = true` (our own endpoints): preserve message/type/code
///   verbatim — the embedded code is real signal we want downstream to
///   see (e.g. retry on 500).
/// - `trusted = false` (third-party gateways): rewrite message to a
///   generic one keyed off the embedded code, and apply
///   [`mask_account_class_status`] so account-class codes (401/402/403/451)
///   surface as 502 — callers must not be able to probe whether the
///   upstream's key was bad or out of credits.
fn try_format_sse_error(value: &serde_json::Value, trusted: bool) -> Option<String> {
    // Require a real error: an object carrying at least a `message` or a
    // `code`. This rejects `error: null` and empty `error: {}` payloads
    // (which some providers emit on healthy chunks) so they aren't mistaken
    // for a failure and turned into a spurious error event.
    let error_obj = value
        .get("error")
        .filter(|v| v.is_object() && (v.get("message").is_some() || v.get("code").is_some()))?;

    if trusted {
        // Strip surrounding chunk fields and emit only the error envelope
        // so `event.data.starts_with("{\"error\"")` in the downstream
        // reassembler matches reliably (OpenRouter-shape chunks would
        // otherwise start with `{"id"`).
        let envelope = json!({ "error": error_obj });
        Some(format!("data: {envelope}"))
    } else {
        // For untrusted providers, replace prose with a generic message
        // and mask account-class status codes to 502 so we don't leak
        // upstream billing/auth state.
        let provider_code = error_obj
            .get("code")
            .and_then(parse_provider_status_code)
            .unwrap_or_else(|| {
                warn!(
                    code = ?error_obj.get("code"),
                    "Provider error object has missing or unrecognized code, defaulting to 500"
                );
                500
            });
        // Derive type/message from the *masked* code (not the original) so the
        // envelope is self-consistent: a masked 402→502 must read as "Bad
        // gateway" and never surface the 402-specific message, which would
        // hint at the hidden billing/auth state we just masked.
        // (401/402/403/451 → 502, 408 → 504; see `mask_account_class_status`.)
        let masked_code = mask_account_class_status(provider_code);
        let (error_type, message) = sanitized_error_for_status(masked_code);
        let envelope = json!({
            "error": {
                "message": message,
                "type": error_type,
                "param": null,
                "code": masked_code,
            }
        });
        Some(format!("data: {envelope}"))
    }
}

/// Extract an HTTP-style status code from a provider error's `code` field.
/// Providers encode it inconsistently: as a number (`429`), a numeric
/// string (`"429"`), or a named string (`"rate_limit"`). Returns `None`
/// when the code is absent or unrecognized so the caller falls back to 500.
///
/// Only the rate-limit family of named codes is mapped (to `429`) because it
/// has unambiguous retry semantics. Other named codes (`insufficient_quota`,
/// `overloaded`, `server_error`, …) intentionally fall back to 500 — mapping
/// them risks incorrect retry behavior, so additions should be deliberate and
/// observed in practice rather than guessed.
fn parse_provider_status_code(code: &serde_json::Value) -> Option<u16> {
    if let Some(n) = code.as_u64() {
        return u16::try_from(n).ok();
    }
    let s = code.as_str()?;
    if let Ok(n) = s.parse::<u16>() {
        return Some(n);
    }
    // Map the rate-limit family to 429 so retry semantics survive; other
    // named codes have no safe numeric mapping and fall back to 500.
    match s {
        "rate_limit" | "rate_limit_error" | "rate_limit_exceeded" => Some(429),
        _ => None,
    }
}

/// Sanitize error responses from third parties
///
/// In strict mode, we never pass through third-party error messages.
/// Instead, we return standard HTTP error responses based on status code.
/// The actual error is logged for debugging but never sent to the client.
async fn sanitize_error_response(mut response: Response) -> Response {
    let status = response.status();

    // Drain the body (bounded, so a misbehaving provider cannot make us buffer
    // an arbitrarily large response) to record its length, but do NOT log its
    // content. ZDR: provider error bodies can echo prompt/response content, so
    // only the status and the buffered length are safe to record.
    const MAX_ERROR_BODY: usize = 64 * 1024;
    let body_bytes =
        match axum::body::to_bytes(std::mem::take(response.body_mut()), MAX_ERROR_BODY).await {
            Ok(bytes) => bytes,
            Err(e) => {
                error!(error = %e, "Failed to read error response body");
                return standard_error_response(status);
            }
        };

    error!(
        status = %status,
        body_len = body_bytes.len(),
        "Third-party error response (logged, not forwarded)"
    );

    // Return standard error based on status code
    standard_error_response(status)
}

/// Map an HTTP status code to a generic (error_type, message) pair.
/// Used by both `standard_error_response` and `try_format_sse_error` to
/// sanitize errors from untrusted providers without leaking internals.
///
/// Callers should apply [`mask_account_class_status`] to the upstream
/// status *before* calling this function so that account-class codes
/// (401/402/403/451) are mapped to 502 first — this function alone does
/// not mask, it only maps a (possibly-already-masked) status to a
/// type/message pair.
fn sanitized_error_for_status(status: u16) -> (&'static str, &'static str) {
    // The 401/402/403/408 arms are defensive. Both production callers
    // (`try_format_sse_error`, `standard_error_response`) run
    // `mask_account_class_status` first (401/402/403/451 -> 502, 408 -> 504),
    // so those statuses are remapped before reaching here. These arms render
    // only if a future caller maps an un-masked status — keeping them avoids
    // falling through to the generic "An error occurred".
    match status {
        400 => ("invalid_request_error", "Invalid request"),
        401 => ("authentication_error", "Authentication failed"),
        402 => ("api_error", "Service unavailable"),
        403 => ("permission_error", "Permission denied"),
        404 => ("not_found_error", "Not found"),
        408 => ("api_error", "Gateway timeout"),
        413 => ("invalid_request_error", "Request too large"),
        422 => ("invalid_request_error", "Unprocessable request"),
        429 => ("rate_limit_error", "Rate limit exceeded"),
        500 => ("api_error", "Internal server error"),
        502 => ("api_error", "Bad gateway"),
        503 => ("api_error", "Service unavailable"),
        504 => ("api_error", "Gateway timeout"),
        _ => ("api_error", "An error occurred"),
    }
}

/// Remap upstream HTTP status codes that would leak provider-side billing,
/// authentication, or jurisdictional state to a single generic 502.
///
/// Onwards routes requests to third-party providers on the operator's
/// behalf — the caller never sees the provider directly. Surfacing the
/// provider's `401 Unauthorized` (our API key revoked), `402 Payment
/// Required` (our credits exhausted), `403 Forbidden` (our org blocked),
/// or `451 Unavailable For Legal Reasons` would let the caller probe
/// the operator's account state, which is both an information leak and
/// confusing (the *caller's* auth is fine — it's ours that isn't). All
/// four collapse to `502 Bad Gateway`: a generic "upstream is sad".
///
/// `408 Request Timeout` is rewritten to `504 Gateway Timeout` for the
/// same reason — the upstream timing out is *our* gateway timing out
/// from the caller's perspective.
///
/// Everything else passes through unchanged: `400/404/413/422/429` are
/// real user-facing signals about the caller's request and must surface,
/// and `5xx` codes are already "upstream failed" semantics.
fn mask_account_class_status(status: u16) -> u16 {
    match status {
        401 | 402 | 403 | 451 => 502,
        408 => 504,
        _ => status,
    }
}

/// Generate standard error response based on HTTP status code, applying
/// account-class masking so the caller can't distinguish account-state
/// failures (auth/billing/permissions) from generic upstream errors.
fn standard_error_response(status: StatusCode) -> Response {
    let masked_code = mask_account_class_status(status.as_u16());
    // `mask_account_class_status` only yields valid codes (the input status or
    // a fixed 502/504), so `from_u16` never fails; the fallback is defensive.
    let masked_status = StatusCode::from_u16(masked_code).unwrap_or(status);
    let (error_type, message) = sanitized_error_for_status(masked_code);
    error_response(masked_status, error_type, message)
}

